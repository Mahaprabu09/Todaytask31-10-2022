package day31102022;

public class Swaptwovalues {
	public static void main(String[]args)
	{
		int a=23;
		int b=33;
		int temp;
		System.out.println("Before swapping of A "+a+" and B "+b);		
		temp=a;
		a=b;
		b=temp;
		System.out.println("Before swapping of A "+a+" and B "+b);
	}

}
